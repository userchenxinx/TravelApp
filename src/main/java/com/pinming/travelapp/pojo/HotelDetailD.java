package com.pinming.travelapp.pojo;

import java.util.Date;

public class HotelDetailD {
    private int id;
    private String hoteldetail;
    private int hoteldetailid;
    private Date hoteltime;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getHoteldetail() {
        return hoteldetail;
    }

    public void setHoteldetail(String hoteldetail) {
        this.hoteldetail = hoteldetail;
    }

    public int getHoteldetailid() {
        return hoteldetailid;
    }

    public void setHoteldetailid(int hoteldetailid) {
        this.hoteldetailid = hoteldetailid;
    }

    public Date getHoteltime() {
        return hoteltime;
    }

    public void setHoteltime(Date hoteltime) {
        this.hoteltime = hoteltime;
    }
}
